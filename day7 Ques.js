let goalsScored = 0; // စစချင်းမှာ သွင်းဂိုး ၀ ဂိုး ရှိပါသေးတယ်
// ၁။ ဒီနေရာမှာ ဘယ်လို အခြေအနေ (condition) စစ်မလဲ? (၃ ဂိုး ထက် ငယ်နေသ၍ ပတ်မယ်)
while (goalsScored < 3){
    // ဂိုးတစ်ဂိုး သွင်းလိုက်တိုင်း golsScored ကို ၁ ဂိုး တိုးပေးရမယ်
    goalsScored++;
    console.log("မန်ယူအသင်း"+ goalsScored + "ဂိုးသွင်းယူလိုက်ပါပြီ။");
}
console.log("Manchester United အသင်း ဗိုလ်စွဲသွားပါပြိ!")